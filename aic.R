x<-runif(100,0,10)
y<-6+5*x+4*x^2+rnorm(100,0,10)
plot(x,y)
model1<-lm(y~x)
summary(model1)
fom1<-function(x){model1$coefficients[1]+model1$coefficients[2]*x}
curve(fom1,0,10,add=TRUE)
resi<-y-fom1(x)
resisd<-sd(resi)
aic<-100*log(2*pi*resisd^2)+100+2*(2+2)
aic

x2<-x^2
model2<-lm(y~x+x2)
summary(model2)
fom2<-function(x){model2$coefficients[1]+model2$coefficients[2]*x+model2$coefficients[3]*x^2}
curve(fom2,0,10,add=TRUE)
resi2<-y-fom2(x)
resisd2<-sd(resi2)
aic2<-100*log(2*pi*resisd2^2)+100+2*(3+2)

x3<-x^3
x4<-x^4
x5<-x^5
model3<-lm(y~x+x2+x3+x4+x5)
c<-model3$coefficients
summary(model3)
fom3<-function(x){c[1]+c[2]*x+c[3]*x^2+c[4]*x^3+c[5]*x^4+c[6]*x^5}
curve(fom3,0,10,add=TRUE)
resi3<-y-fom3(x)
resisd3<-sd(resi3)
aic3<-100*log(2*pi*resisd3^2)+100+2*(6+2)
